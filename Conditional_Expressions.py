# Conditional expressions = A one-line shortcut  for the if-else statement (ternary operator).

Num1 = float(input("Enter the first number:- "))
Num2 = float(input("Enter the second number:- "))
print("The first number you assigned is Positve." if Num1 > 0  else "The first number you assigned is Negetive.")
print("The second number you assigned is Positive" if Num2 > 0 else "The second number you assigned is Negetive")
Value = int(input("Enter the value:- "))
Result = "The value you assigned is odd number." if Value % 2 == 0 else "The value you assigned is even number."
print(Result)



